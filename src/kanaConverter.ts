export const kanaToKorean = (text: string): string => {
  let result = '';
  let i = 0;
  
  while (i < text.length) {
    // 2글자 요음 확인
    const twoChars = text.substring(i, i + 2);
    if (kanaToKoreanMap[twoChars]) {
      result += kanaToKoreanMap[twoChars];
      i += 2;
    } else {
      // 1글자 처리
      const oneChar = text[i];
      result += kanaToKoreanMap[oneChar] || oneChar;
      i++;
    }
  }
  
  // 촉음(っ) 처리: 뒤에 오는 자음을 앞글자 받침으로 넣어야 하지만 초경량 버전에선 공백이나 느낌만 살림
  return result;
};
